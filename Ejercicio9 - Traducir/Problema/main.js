var palabra;
palabra = prompt("Ingrese palabra a traducir. Opciones: casa, mesa, perro o gato");

switch (palabra) {
  case "hola":
  alert("hello");
  break
  case "mesa":
  alert("table");
  break
  case "perro":
  alert("dog");
  break
  case "gato":
  alert("cat");
  break
  default:
  alert("no se encontro la palabra");
}



